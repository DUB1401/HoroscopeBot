# –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä –≥–æ—Ä–æ—Å–∫–æ–ø–æ–≤.
class Horoscope:
	
	# –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –≥–æ—Ä–æ—Å–∫–æ–ø.
	def __GetHoroscope(self, Zodiac: str) -> str:
		# –í—ã–≤–æ–¥ –≤ –∫–æ–Ω—Å–æ–ª—å: –ø—Ä–æ–≥—Ä–µ—Å—Å.
		print(f"Updating horoscope for zodiac: \"{Zodiac}\".")
		# –ü—Ä–∏–≤–µ–¥–µ–Ω–∏–µ –∑–Ω–∞–∫–∞ –∑–æ–¥–∏–∞–∫–∞ –∫ –Ω–∏–∂–Ω–µ–º—É —Ä–µ–≥–∏—Å—Ç—Ä—É.
		Zodiac = Zodiac.lower()
		# –ì–æ—Ä–æ—Å–∫–æ–ø.
		Text = ""
		# –ú–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä—ã –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏—è.
		Modificators = [
			["(–Ω–µ–π—Ç—Ä–∞–ª—å–Ω—ã–π)", "(–Ω–µ–π—Ç—Ä–∞–ª—å–Ω—ã–π)", "(–Ω–µ–π—Ç—Ä–∞–ª—å–Ω—ã–π)"],
			["(–ø–æ–∑–∏—Ç–∏–≤–Ω—ã–π)", "(–ø–æ–∑–∏—Ç–∏–≤–Ω—ã–π)", "(–Ω–µ–≥–∞—Ç–∏–≤–Ω—ã–π)"],
			["(–Ω–µ–π—Ç—Ä–∞–ª—å–Ω—ã–π)", "(–Ω–µ–π—Ç—Ä–∞–ª—å–Ω—ã–π)", "(–Ω–µ–≥–∞—Ç–∏–≤–Ω—ã–π)"],
			["(–Ω–µ–π—Ç—Ä–∞–ª—å–Ω—ã–π)", "(–ø–æ–∑–∏—Ç–∏–≤–Ω—ã–π)", "(–Ω–µ–≥–∞—Ç–∏–≤–Ω—ã–π)"],
		]
		# –¢–µ–∫—É—â–∏–π –º–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä.
		CurrentModificator = random.choice(Modificators)
		# –ü–µ—Ä–µ–º–µ—à–∏–≤–∞–Ω–∏–µ –º–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä–æ–≤ —Å–ª—É—á–∞–π–Ω—ã–º –æ–±—Ä–∞–∑–æ–º.
		random.shuffle(CurrentModificator)
		
		# –°–æ—Å—Ç–∞–≤–ª–µ–Ω–∏–µ –∑–∞–ø—Ä–æ—Å–∞.
		Request = f"–°–æ—Å—Ç–∞–≤—å —É–Ω–∏–∫–∞–ª—å–Ω—ã–π –Ω–µ–ø–æ–≤—Ç–æ—Ä–∏–º—ã–π –≥–æ—Ä–æ—Å–∫–æ–ø –Ω–∞ —Å–µ–≥–æ–¥–Ω—è—à–Ω–∏–π –¥–µ–Ω—å –¥–ª—è –∑–Ω–∞–∫–∞ –∑–æ–¥–∏–∞–∫–∞ {Zodiac}. –ì–æ—Ä–æ—Å–∫–æ–ø –¥–æ–ª–∂–µ–Ω –æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ –∏–º–µ—Ç—å —Å–ª–µ–¥—É—é—â–∏–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏: –ª–∏—á–Ω–∞—è –∂–∏–∑–Ω—å {CurrentModificator[0]}, –∫–∞—Ä—å–µ—Ä–∞ {CurrentModificator[1]}, –∑–¥–æ—Ä–æ–≤—å–µ {CurrentModificator[2]}!"
		
		# –ü–æ–∫–∞ –Ω–µ –ø–æ–ª—É—á–µ–Ω —É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä–∏—Ç–µ–ª—å–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç.
		while "–ª–∏—á–Ω–∞—è –∂–∏–∑–Ω—å" not in Text.lower() or "–∫–∞—Ä—å–µ—Ä–∞" not in Text.lower() or "–∑–¥–æ—Ä–æ–≤—å–µ" not in Text.lower():
			# –í—ã–≤–æ–¥ –≤ –∫–æ–Ω—Å–æ–ª—å: –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –∑–∞–ø—Ä–æ—Å–∞.
			print(f"Requesting by " + self.__Settings["lib"] + "...")
			
			# –ï—Å–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ freeGPT.
			if self.__Settings["lib"] == "freeGPT":
				# –í—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –∑–∞–ø—Ä–æ—Å–∞.
				Response = Client.create_completion("gpt4", Request)
				# –ò–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è –∑–∞–ø—Ä–æ—Å–∞.
				Text = Response.encode("utf-8").decode("unicode-escape")
			
			# –ï—Å–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ g4f.
			elif self.__Settings["lib"] == "g4f":
				
				try:
					# –í—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –∑–∞–ø—Ä–æ—Å–∞.
					Text = g4f.ChatCompletion.create(model = g4f.models.gpt_4, provider = g4f.Provider.GeekGpt, messages = [{"role": "user", "content": Request}])
					
				except:
					# –í—ã–∂–∏–¥–∞–Ω–∏–µ –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞.
					sleep(self.__Settings["delay"])
				
			else:
				# –í—ã–±—Ä–æ—Å –∏—Å–∫–ª—é—á–µ–Ω–∏—è.
				raise Exception("Unsupported GPT-4 lib: \"" + self.__Settings["lib"] + "\".")
				
		# –í—ã–≤–æ–¥ –≤ –∫–æ–Ω—Å–æ–ª—å: –∑–∞–≤–µ—Ä—à–µ–Ω–∏–µ.
		print("Done.")
		# –£–¥–∞–ª–µ–Ω–∏–µ –º–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä–æ–≤ –ø—Ä–æ–≥–Ω–æ–∑–∞.
		Text = RemoveRegexSubstring(Text, ".?\([–ü–ø–ù–Ω][–æ–µ][–∑–≥][–∏–∞]—Ç–∏–≤–Ω—ã–π\):?")
		Text = RemoveRegexSubstring(Text, ".?\([–ù–Ω]–µ–π—Ç—Ä–∞–ª—å–Ω—ã–π\):?")
		# –û—á–∏—Å—Ç–∫–∞ –º–∞—Ä–∫–∏—Ä–æ–≤–∫–∏.
		Text = Text.replace("**", "")
		
		return Text
	
	# –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä.
	def __init__(self, Settings: dict):
		
		#---> –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–∏—Ö —Å–≤–æ–π—Å—Ç–≤.
		#==========================================================================================#
		# –ì–ª–æ–±–∞–ª—å–Ω—ã–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏.
		self.__Settings = Settings.copy()
		# –ß—Ç–µ–Ω–∏–µ —Ñ–∞–π–ª–∞ –≥–æ—Ä–æ—Å–∫–æ–ø–∞.
		self.__Horoscope = ReadJSON("Data/Horoscope.json")
		
	# –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç—Ä–æ–∫–æ–≤—É—é –¥–∞—Ç—É.
	def getDate(self) -> str:
		return self.__Horoscope["date"]

	# –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ç–µ–∫—Å—Ç—ã –≥–æ—Ä–æ—Å–∫–æ–ø–æ–≤.
	def getHoroscopes(self) -> dict:
		return self.__Horoscope["horoscopes"]
	
	# –û–±–Ω–æ–≤–ª—è–µ—Ç –≥–æ—Ä–æ—Å–∫–æ–ø—ã –Ω–∞ —Å–µ–≥–æ–¥–Ω—è—à–Ω–∏–π –¥–µ–Ω—å.
	def update(self):
		# –ó–∞–ø–∏—Å—å –¥–∞—Ç—ã –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è.
		self.__Horoscope["date"] = str(datetime.datetime.now().strftime("%d.%m.%Y"))
		
		# –î–ª—è –∫–∞–∂–¥–æ–≥–æ –∑–Ω–∞–∫–∞ –∑–æ–¥–∏–∞–∫–∞.
		for Key in self.__Horoscope["horoscopes"].keys():
			# –ü–æ–ª—É—á–µ–Ω–∏–µ –≥–æ—Ä–æ—Å–∫–æ–ø–∞.
			Text = self.__GetHoroscope(Key)
			
			# –î–ª—è –∫–∞–∂–¥–æ–≥–æ –∞–±–∑–∞—Ü–∞.
			for Paragraph in Text.split("\n\n"):
				# –û—á–∏—Å—Ç–∫–∞ –ø–æ—Ç–æ—Ä—è—é—â–∏—Ö—Å—è –ø—Ä–æ–±–µ–ª–æ–≤.
				Bufer = RemoveRecurringSubstrings(Paragraph.strip(), " ")
				# –£–¥–∞–ª–µ–Ω–∏–µ —Å–∏–º–≤–æ–ª–æ–≤ –Ω–æ–≤–æ–π —Å—Ç—Ä–æ–∫–∏ –∏ —ç–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ.
				Bufer = EscapeCharacters(Bufer.replace("\n", ""))
				
				# –ï—Å–ª–∏ –ø–∞—Ä–∞–≥—Ä–∞—Ñ –æ–ø–∏—Å—ã–≤–∞–µ—Ç –ª–∏—á–Ω—É—é –∂–∏–∑–Ω—å.
				if Bufer.startswith("–õ–∏—á–Ω–∞—è –∂–∏–∑–Ω—å"):
					# –ë—É—Ñ–µ—Ä –≥–æ—Ä–æ—Å–æ–∫–æ–ø–∞.
					Bufer = RemoveRegexSubstring(Bufer, "^[–õ–ª]–∏—á–Ω–∞—è –∂–∏–∑–Ω—å").strip(": \n")
					# –ï—Å–ª–∏ –±—É—Ñ–µ—Ä –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å —Å–∏–º–≤–æ–ª–∞ –≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ, –¥–æ–±–∞–≤–∏—Ç—å –∫–æ–ø–∏—é –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä–∞.
					if Bufer[0].islower() == True: Bufer = "–õ–∏—á–Ω–∞—è –∂–∏–∑–Ω—å " + Bufer
					# –ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –ø–æ–ª—è –ª–∏—á–Ω–æ–π –∂–∏–∑–Ω–∏.
					self.__Horoscope["horoscopes"][Key]["love"] = "üíû _*–õ–∏—á–Ω–∞—è –∂–∏–∑–Ω—å:*_\n" + Bufer
					
				# –ï—Å–ª–∏ –ø–∞—Ä–∞–≥—Ä–∞—Ñ –æ–ø–∏—Å—ã–≤–∞–µ—Ç –ª–∏—á–Ω—É—é –∂–∏–∑–Ω—å.
				if Bufer.startswith("–ö–∞—Ä—å–µ—Ä–∞"):
					# –ë—É—Ñ–µ—Ä –≥–æ—Ä–æ—Å–æ–∫–æ–ø–∞.
					Bufer = RemoveRegexSubstring(Bufer, "^[–ö–∫]–∞—Ä—å–µ—Ä–∞").strip(": \n")
					# –ï—Å–ª–∏ –±—É—Ñ–µ—Ä –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å —Å–∏–º–≤–æ–ª–∞ –≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ, –¥–æ–±–∞–≤–∏—Ç—å –∫–æ–ø–∏—é –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä–∞.
					if Bufer[0].islower() == True: Bufer = "–ö–∞—Ä—å–µ—Ä–∞ " + Bufer
					# –ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –ø–æ–ª—è –ª–∏—á–Ω–æ–π –∂–∏–∑–Ω–∏.
					self.__Horoscope["horoscopes"][Key]["career"] = "üíº _*–ö–∞—Ä—å–µ—Ä–∞:*_\n" + Bufer
					
				# –ï—Å–ª–∏ –ø–∞—Ä–∞–≥—Ä–∞—Ñ –æ–ø–∏—Å—ã–≤–∞–µ—Ç –ª–∏—á–Ω—É—é –∂–∏–∑–Ω—å.
				if Bufer.startswith("–ó–¥–æ—Ä–æ–≤—å–µ"):
					# –ë—É—Ñ–µ—Ä –≥–æ—Ä–æ—Å–æ–∫–æ–ø–∞.
					Bufer = RemoveRegexSubstring(Bufer, "^[–ó–∑]–¥–æ—Ä–æ–≤—å–µ").strip(": \n")
					# –ï—Å–ª–∏ –±—É—Ñ–µ—Ä –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å —Å–∏–º–≤–æ–ª–∞ –≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ, –¥–æ–±–∞–≤–∏—Ç—å –∫–æ–ø–∏—é –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä–∞.
					if Bufer[0].islower() == True: Bufer = "–ó–¥–æ—Ä–æ–≤—å–µ " + Bufer
					# –ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –ø–æ–ª—è –ª–∏—á–Ω–æ–π –∂–∏–∑–Ω–∏.
					self.__Horoscope["horoscopes"][Key]["health"] = "üíä _*–ó–¥–æ—Ä–æ–≤—å–µ:*_\n" + Bufer
					
			# –í—ã–∂–∏–¥–∞–Ω–∏–µ –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞.
			sleep(self.__Settings["delay"])

		# –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö.
		WriteJSON("Data/Horoscope.json", self.__Horoscope)